void solve() {
    int n, m;
    cin >> n >> m;
    vector<string> g(n);
    pii start, end;
    for (int i = 0; i < n; i++) {
        cin >> g[i];
        for (int j = 0; j < m; j++) {
            if (g[i][j] == 'S') {
                start = {i, j};
            }
            if (g[i][j] == 'G') {
                end = {i, j};
            }
        }
    }

    int dx[4] = {-1, 1, 0, 0};
    int dy[4] = {0, 0, -1, 1};

    auto isSafe = [&](int x, int y) -> bool {
        return x >= 0 && y >= 0 && x < n && y < m && g[x][y] != '#';
    };

    // Visited array to store if we have visited (x, y) in a certain level
    int vis[n][m][2];
    memset(vis, 0, sizeof(vis));

    // Queue stores: {{x, y}, {distance, level}}
    queue<pair<pair<int, int>, pair<int, int>>> q;

    // Initial state
    q.push({start, {0, 0}});
    vis[start.first][start.second][0] = 1; // **FIX 1: Mark initial state as visited**

    while (!q.empty()) {
        pii node = q.front().first;
        int dis = q.front().second.first;
        int lvl = q.front().second.second;
        q.pop();

        // Check for goal
        if (node == end) {
            cout << dis << endl;
            return;
        }

        // Explore neighbors
        for (int k = 0; k < 4; k++) {
            int nx = node.first + dx[k];
            int ny = node.second + dy[k];

            if (isSafe(nx, ny)) {
                // Check if move is allowed based on current level
                if (lvl == 0 && g[nx][ny] == 'x') continue;
                if (lvl == 1 && g[nx][ny] == 'o') continue;

                // **FIX 2: Determine the level of the *next* state**
                int next_lvl = lvl;
                if (g[nx][ny] == '?') {
                    next_lvl = 1 - lvl; // Flip the level
                }

                // **FIX 1 (continued): Check visited status for the NEXT state and mark *before* pushing**
                if (!vis[nx][ny][next_lvl]) {
                    vis[nx][ny][next_lvl] = 1; // Mark as visited
                    q.push({{nx, ny}, {dis + 1, next_lvl}});
                }
            }
        }
    }

    cout << -1 << endl;
}
   
   
   
